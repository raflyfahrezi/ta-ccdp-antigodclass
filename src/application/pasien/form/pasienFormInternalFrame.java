    @Override
    public void showData(pasien pasien) {
        
        txtNama.setText(pasien.getNama());
        txtAlamat.setText(pasien.getAlamat());
        formattedTxtUmur.setText(pasien.getUmur());
        
        DefaultComboBoxModel defaultComboBoxModel = (DefaultComboBoxModel) cbxJenisKelamin.getModel();
        
        for (int i = 0; i < defaultComboBoxModel.getSize(); i++) {
            if (defaultComboBoxModel.getElementAt(i).equals(pasien.getJenis_kelamin())); {
                defaultComboBoxModel.setSelectedItem(i);
            }
        }
    }
    
