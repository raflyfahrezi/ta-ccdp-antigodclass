    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtNama.getText().isEmpty()){
            showError("Nama tidak boleh kosong!");
        }
        else if (cbxJenisKelamin.getSelectedItem().equals("-")) {
            showError("Jenis Kelamin tidak boleh kosong");
        }
        else if (txtAlamat.getText().isEmpty()){
            showError("Alamat tidak boleh kosong!");
        } else if (formattedTxtUmur.getText().isEmpty()) {
            showError("Umur tidak boleh kosong");
        }
        else {
            setWaitCursor();
            presenter.save();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed
    
